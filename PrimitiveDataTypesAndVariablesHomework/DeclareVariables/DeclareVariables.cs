using System;

namespace DeclareVariables
{
    class DeclareVariables
    {
        static void Main()
        {
            ushort number1 = 52130;
            Console.WriteLine(number1);

            sbyte number2 = -115;
            Console.WriteLine(number2);

            int number3 = 4825932;
            Console.WriteLine(number3);

            byte number4 = 97;
            Console.WriteLine(number4);

            short number5 = -10000;
            Console.WriteLine(number5);
        }
    }
}
